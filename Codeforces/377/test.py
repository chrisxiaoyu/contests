import a
import unittest


class TestContest377(unittest.TestCase):

    def test_problem_a(self):
        self.assertEqual(
            a.solve(
                3,
                4,
                2,
                [
                    ['#', '.', '.', '#'],
                    ['.', '.', '#', '.'],
                    ['#', '.', '.', '.']
                ]
            ),
            [
                ['#', '.', '.', '#'],
                ['.', '.', '#', 'X'],
                ['#', '.', '.', 'X']
            ]
        )
        self.assertEqual(
            a.solve(
                5,
                4,
                5,
                [
                    ['#', '.', '.', '.'],
                    ['#', '.', '#', '.'],
                    ['.', '#', '.', '.'],
                    ['.', '.', '.', '#'],
                    ['.', '#', '.', '#']
                ]
            ),
            [
                ['#', '.', '.', '.'],
                ['#', 'X', '#', '.'],
                ['X', '#', '.', '.'],
                ['X', '.', '.', '#'],
                ['X', '#', 'X', '#']
            ]
        )


if __name__ == '__main__':
    unittest.main()
